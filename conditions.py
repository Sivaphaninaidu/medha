
# // Asks the user to enter a number from 1 to 7

# // Uses conditionals to display the corresponding day of the week

# // Handles invalid inputs with an appropriate message



number = int(input("enter the number: "))
if number == 1:
    print("sunday")
elif number == 2:
    print("monday")
elif number == 3:
    print("tuesday")
elif number == 4:
    print("wednesday")
elif number == 5:
    print("thursday")
elif number == 6:
    print("friday")
elif number == 7:
    print("saturday")
else :
    print("this is put of the week")

